3,5G终端对高性能器件需求升级,公司产品ASP提升
	1,功能件行业技术壁垒高,客户导向型,行业整合
		功能器件制造厂商技术要求高,客户导向型呈现行业整合趋势.
			大多数精密功能器件的生产方案都需要与下游整机厂商共同开发、设计、定制产品技术参数,属于紧密型生产合作的产业
			客户导向型行业,精密功能器件厂商对行业内的优质资源不断整合
		未来朝高精密、高性能方向发展.消费智能终端产品逐步向轻薄化、便携化和高性能发展,从而带动精密功能器件向高集成、高性能方向发展.
			高性能要求单一产品实现功能更多、运算速度更快,也会形成更高的功耗,因此要求内部组件散热性、可靠性和耐疲劳性更好
			高性能、高精密度的产品需求将对行业企业的加工技术、加工精度和组装能力提出更高要求;有利于设备精良、资金和技术实力强的企业获得更多市场份额
	2,精密功能件需求增加,公司产品单机价值量将进一步提升
		5G发展迅猛的势头将推动智能手机市场快速复苏,未来3年出货量将维持正增长,IDC预计在2021年全球手机出货量同比将增长5.6%
		手机功能创新下各类精密功能件需求提升
			多摄、无线充电、屏幕方案升级、生物监测等功能,激发更多精密功能件需求
			多摄,刺激固定支撑功能件;磁干扰刺激金属导磁材料需求;光学防抖功能刺激手机心田固定板和弹片需求
			OLED屏幕减少背光模组取消,减少内部偏光片、封装玻璃;刺激偏光膜、保护膜等柔性膜材
			全面屏机型中3Dtouch部分需添加铝箔以抗电磁干扰,刺激铝箔
			无线充电模组:刺激芯片、线圈、磁性材料等
			小件:刺激防尘网、过滤网、防护膜、防水橡胶垫等
		手机功能件单机价值量增加,iPhone中领益科技产品单机价值将继续提升
			自2008年起进入苹果功能件供应链中,苹果手机创新发展的同时,对功能件标准也同步提高,公司具备技术优势与生产经验,未来有望进一步提升供货份额
			虽然单机价值低,但是安卓出货量体量大;领益科技于2014年起进入头部安卓厂商vivo、OPPO、华为、小米以及三星、LG等品牌的供应链
		智能手机散热方案不断升级,公司募资投向散热模组增加业绩收入
			原智能手机散热方案以导热材料石墨片、散热胶等为主,5G时代到来后包括散热管、均热板(VaporChamber,VC)在内的液冷散热成为5G手机的首选散热方案
