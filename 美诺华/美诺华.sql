原研药主要指首家研发上市的药品，原研药已过了专利保护期的，其它企业可以进行仿制。
仿制药是与原研药具有相同的活性成分、剂型、给药途径和治疗作用的药品。通过了一致性评价的仿制药是与原研药药学等效、生物等效(即质量和疗效一致)，但成本比原研药低的等效药品，在临床上可以实现与原研药的相互替代。
所谓"仿制药质量和疗效一致性评价"，就是要求对已经批准上市的仿制药品，在质量和疗效上与原研药能够一致，在临床上与原研药可以相互替代
"创新药"是指具有自主知识产权的药物，是一类创新性药物，它是在已有药物的基础上做一些改进，比如通过结构修饰，使药物的生物利用率更高。相对于"原研药"和"仿制药"而言，"创新药"强调新颖的化学结构或新的治疗用途，并且能够打破"原研药"的专利壁垒。

