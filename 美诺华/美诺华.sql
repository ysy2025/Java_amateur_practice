			以附加值较高的技术输出取代单纯的产能输出(CMO 代工)，是当前医药领域最火热的领域之一
			医药研发产业链:CRO(临床前研发)->CDMO(研采产一体化)->CMO生产
			创新药领域不断发展、研发成本和风险居高不下->越来越多的下游医药制造企业会选择 CDMO 服务，以便为医药企业节约成本、控制风险并提升研发效率

