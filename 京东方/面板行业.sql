		在全球液晶面板市场,我们面板产业产能占比接近七成,成为全球第一大面板产业集中地;洗出日韩台落后产能
		大陆液晶面板产业从无到强的成功是中国半导体崛起的第一步,发展面板产业不仅是经济需要,更是国家产业安全的需要.
		伴随国产面板龙头厂商在面板产业的话语权逐渐增强,未来大陆面板产业将迎来黄金发展期

	产业转移,国产面板份额提升,行业快速上升通道
		京东方,TCL,深天马,维信诺
		国产龙头盈利确定性高;新领域,国产厂商也在加速追赶

	材料市场,国产替代
		全球显示材料市场规模预计约3000 亿元,包括玻璃基板、偏光片、彩色滤光片、驱动 IC
		我们预计随着面板产业逐步向大陆集中,国产龙头厂商掌握行业话语权,将加速推进产业链国产替代进程.
		叠加面板行业整体的景气度上行,我们认为国产面板材料厂商将长期受益

		三利谱、八亿时空、激智科技、杉杉股份、万润股份、双星新材

