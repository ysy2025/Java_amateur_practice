双星新材

0,核心逻辑
	低估值源于19年技改后新材料和传统板块同时上行中,投资人无法证伪周期和成长,20年公司新材料板块占毛利比重已达56%;即将交付的新产能都是新材料领域,存在预期差。
	17-18年工艺突破, 19年完成产能技改匹配新产品,目前国内外客户部分放量;预计后期放量,公司秉承切片-基膜-成品膜的商业模式,新产能达产后弹性巨大
	基膜决定了高端成品膜的品质,双星在基膜技术突破后具备三个优势:
		 1)成本优势; 
		 2)根据终端客户快速调整配方、工艺进行送样测试;
		 3)产能储备保障了头部客户交付量
	新材料板块-光学显示膜:主要产品为附加值较高的复合膜、增亮膜、扩散膜
	新材料板块-光学非显示膜:高端MLCC、窗膜的基膜、成品膜市场空间和国产替代潜力巨大;目前已向海外客户供货, 2021年开始突破国内市场
	新材料板块-光伏:下游需求较为刚性,门槛在于产品稳定性

	核心逻辑是以面板为代表的新产业转移至中国,国内在制造业升级中,用膜产业链上会逐步诞生一些优异的供应商,伴随双星的技术突破和产能放量,公司盈利和估值体系的双升

1,行业:聚酯薄膜(BOPET):传统的周期和新材料的成长
	2020年中国BOPET整体产能近349万吨,产量276万吨,下游需求以包装印刷为主,占比61%;
	但伴随产业升级,电子电气、新能源、环保、家具装饰对新型BOPET功能膜的需求日益旺盛,这导致我国BOPET行业近年来不断的转型升级
	2020年行业产能开工未满的情况下,每年有近12-14%的高端产品仍需要进口
	行业的逻辑既有传统产品的周期特点,也有新材料成长的属性;
	涉及新材料、进口替代、产业链快速放量属性的公司更具成长属性

