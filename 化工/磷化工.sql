磷酸盐行业格局
	磷酸铁锂：前景良好,供给快速扩张
		磷酸铁锂是主要的正极材料之一。 正极材料是决定锂离子电池性能的关键材料之一,也是目前商业化锂离子电池中主要的锂离子来源,其性能和价格对锂离子电池的影响较大。
		磷酸铁锂的属性良好,其循环性能稳定,安全性好,可大电流充放电,内阻小发热少,安全,原材料来源广泛,价格便宜,环保无毒、无污染
		我国磷酸铁锂产能持续扩张

	饲料磷酸钙：我国饲料磷酸钙行业已步入成熟期
		磷酸氢钙（DCP）、磷酸二氢钙（MCP）等饲料磷酸钙盐因为在家畜家禽饲料添加剂中具有不可替代的作用,而成为大宗磷酸钙盐产品。

