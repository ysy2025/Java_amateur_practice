1) 价格是化工行业景气度的重要指标，当前部分产品价格处于历史高位，但仍有大量石油化工产
品的价格上行空间较大，我们建议把握那些价格仍处于中低位的化工产品及相关子行业（例如
PTA 和涤纶长丝）；以及近期由于电力因素而涨价的磷肥和磷化工板块，推荐荣盛石化、恒力
石化、新凤鸣；短期建议关注兴发集团和云天化。
2) 部分特种化学品和化工新材料行业的毛利率受原材料价格影响较小（成本完全可以传导出去），
另一部分特种化学品和化工新材料行业虽然受原材料价格影响较大，但当原材料价格下行时，
企业的毛利率会有所提升，从而出现周期性投资机会。我们建议关注符合此逻辑的改性塑料和
新材料板块，推荐光威复材，建议关注国瓷材料、金发科技等企业。