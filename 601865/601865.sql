3,竞争格局:先发优势,成本曲线陡峭,双寡头
	行业壁垒:技术,认证,规模效应
		技术方面,超白压延玻璃,透光率,反射率,耐腐蚀,耐高温;同样,超白浮法玻璃也可以,;但是浮法玻璃综合透光率低一些
		先发优势明显,壁垒较高.玻璃的光能透过率、吸收率、反射率、导电性能、抗冲击性、耐腐蚀性、耐高温性、使用寿命等方面有更高要求.
		普通浮法玻璃很难和光伏玻璃切换;浮法玻璃企业很难进入光伏玻璃行业

		光伏玻璃包括原片+深加工;技术壁垒高

