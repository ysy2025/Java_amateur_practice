4,企业
	福莱特玻璃集团股份有限公司是一家集玻璃研发、制造、加工和销售为一体的综合性中大型企业,集团本部位于长三角经济中心浙江嘉兴市.
	公司成立于 1998 年 6 月,从玻璃贸易起家,逐步转向玻璃加工生产,2006 年开始涉足光伏玻璃行业.
	上海福莱特生产的光伏玻璃在国内率先通过瑞士 SPF 认证,打破了该领域的国际垄断. 
	2008-2010年嘉福三条共 1090t/d 的光伏玻璃产线依次投产; 2011-2013 年集团本部两条各 600t/d 光伏玻璃产线依次投产,扩大了光伏玻璃主业的业务规模

